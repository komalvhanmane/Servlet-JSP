package com.model;

public class History {
	private String str;
	private String date;
	public History(String str, String date) {
		super();
		this.str = str;
		this.date = date;
	}
	public String getStr() {
		return str;
	}
	public void setStr(String str) {
		this.str = str;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	
}
